fracoContra(P, T) :-
    pokemonType(P, X, Y),
    (fraqueza(X, T); fraqueza(Y, T)),
    \+ resistente(X, T),
    \+ resistente(Y, T).

forteContra(P, T) :-
    pokemonType(P, X, Y),
    (resistente(X, T); resistente(Y, T)),
    \+ fraqueza(X, T),
    \+ fraqueza(Y, T).

